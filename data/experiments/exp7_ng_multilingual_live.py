# -*- coding: utf-8 -*-
"""
Exp 7: Nigeria multilingual live-web test.

Tests whether DuckDuckGo returns usable organization results for
Yoruba and Hausa queries generated by build_local_queries('NG').

Imports search_ddg_html from run_researcher_ng (no duplication of scraper code).
Reports query text, HTTP result counts, first 3 titles/URLs, and a
simple Nigerian-org heuristic.
"""

import os
import sys
import time

_DATA_DIR = os.path.dirname(os.path.abspath(__file__))
_DATA_DIR = os.path.dirname(_DATA_DIR)  # experiments/ -> data/
if _DATA_DIR not in sys.path:
    sys.path.insert(0, _DATA_DIR)

from i18n_terms import build_local_queries    # noqa: E402
from run_researcher_ng import search_ddg_html  # noqa: E402

SLEEP_BETWEEN = 2  # seconds - polite DDG rate limit
MAX_RESULTS_SHOWN = 3

# Nigerian org heuristics (title or URL)
NG_INDICATORS = ["nigeria", "nigerian", "ngo", "foundation", "cooperative",
                 "association", "cooperative", "trust", ".ng"]


def _looks_nigerian(title, url, snippet):
    combined = (title + " " + url + " " + snippet).lower()
    return any(ind in combined for ind in NG_INDICATORS)


def _pick_nonnglish_queries(country_code, lang_code, n=3):
    """
    Pull up to n queries from build_local_queries for a specific language
    that contain at least one non-ASCII character (proxy for non-English words).
    Falls back to queries containing the language code hint if no non-ASCII found.
    """
    all_queries = build_local_queries(country_code)
    non_english = []
    for q in all_queries:
        # Check if query has non-ASCII characters (native script terms)
        has_non_ascii = any(ord(ch) > 127 for ch in q)
        if has_non_ascii:
            non_english.append(q)
        if len(non_english) >= n:
            break

    if len(non_english) < n:
        # Fallback: queries that clearly contain Yoruba/Hausa keywords
        lang_keywords = {
            'yo': ['egbe', 'owe', 'esusu', 'bayanihan', 'damayan'],
            'ha': ['gayya', 'kungiya', 'kungiyar', 'hausa'],
        }
        kws = lang_keywords.get(lang_code, [])
        for q in all_queries:
            if any(kw in q.lower() for kw in kws) and q not in non_english:
                non_english.append(q)
            if len(non_english) >= n:
                break

    return non_english[:n]


def _test_query(query, query_num, total):
    print(f"\n[{query_num}/{total}] Query: {query!r}")
    try:
        results = search_ddg_html(query)
        success = True
    except Exception as exc:
        print(f"  ERROR: {exc}")
        return {
            "query": query,
            "success": False,
            "result_count": 0,
            "results": [],
            "ng_hits": 0,
        }

    print(f"  HTTP success: True | Results: {len(results)}")
    ng_hits = 0
    shown = []
    for i, r in enumerate(results[:MAX_RESULTS_SHOWN]):
        title = r.get("title", "") or ""
        url = r.get("url", "") or ""
        snippet = r.get("snippet", "") or ""
        is_ng = _looks_nigerian(title, url, snippet)
        if is_ng:
            ng_hits += 1
        flag = " [NG-MATCH]" if is_ng else ""
        print(f"  [{i+1}] {title[:70]}{flag}")
        print(f"       {url[:70]}")
        shown.append({"title": title, "url": url, "ng": is_ng})

    # Count total NG matches across all results (not just shown)
    for r in results:
        t = r.get("title", "") or ""
        u = r.get("url", "") or ""
        s = r.get("snippet", "") or ""
        if _looks_nigerian(t, u, s):
            ng_hits += 1
    # Deduplicate count - just count from all results directly
    ng_hits = sum(1 for r in results
                  if _looks_nigerian(
                      r.get("title",""), r.get("url",""), r.get("snippet","")))
    print(f"  Nigerian-org heuristic hits: {ng_hits}/{len(results)}")

    return {
        "query": query,
        "success": True,
        "result_count": len(results),
        "results": shown,
        "ng_hits": ng_hits,
    }


def run():
    print("=== Exp 7: Nigeria multilingual live-web test ===\n")

    # --- Pick Yoruba queries ---
    yo_queries = _pick_nonnglish_queries('NG', 'yo', 3)
    ha_queries = _pick_nonnglish_queries('NG', 'ha', 3)

    print(f"Yoruba queries selected ({len(yo_queries)}):")
    for q in yo_queries:
        print(f"  {q!r}")
    print(f"Hausa queries selected ({len(ha_queries)}):")
    for q in ha_queries:
        print(f"  {q!r}")

    all_queries = [(q, 'yo') for q in yo_queries] + [(q, 'ha') for q in ha_queries]
    total = len(all_queries)
    results_log = []

    for i, (query, lang) in enumerate(all_queries, 1):
        print(f"\n--- Language: {lang.upper()} ---")
        result = _test_query(query, i, total)
        result["lang"] = lang
        results_log.append(result)
        if i < total:
            print(f"  (sleeping {SLEEP_BETWEEN}s...)")
            time.sleep(SLEEP_BETWEEN)

    # --- Verdict ---
    print("\n\n=== VERDICT ===\n")
    total_results = sum(r["result_count"] for r in results_log)
    total_ng_hits = sum(r["ng_hits"] for r in results_log)
    successful = sum(1 for r in results_log if r["success"])

    print(f"Queries run       : {total}")
    print(f"HTTP successes    : {successful}/{total}")
    print(f"Total result rows : {total_results}")
    print(f"NG-org heuristic  : {total_ng_hits} results matched Nigerian-org indicators")

    # Per-language breakdown
    for lang in ['yo', 'ha']:
        lang_results = [r for r in results_log if r["lang"] == lang]
        lang_ng = sum(r["ng_hits"] for r in lang_results)
        lang_total = sum(r["result_count"] for r in lang_results)
        lang_name = "Yoruba" if lang == 'yo' else "Hausa"
        print(f"\n  {lang_name}: {lang_total} results, {lang_ng} NG-heuristic hits")
        for r in lang_results:
            status = "OK" if r["success"] else "FAIL"
            print(f"    [{status}] {r['query'][:60]!r} -> {r['result_count']} results, {r['ng_hits']} NG-hits")

    # Honest assessment
    print("\n--- Honest verdict ---")
    if total_results == 0:
        print("RESULT: No results returned for any query.")
        print("Likely cause: DDG is blocking scraped requests, or native-script")
        print("queries are being dropped/rejected by the HTML endpoint.")
        print("Recommended fix: Try appending 'site:ng OR Nigeria' to queries (already")
        print("done in search_ddg_html), test with a Yoruba-specific portal like")
        print("yoruba.edu.ng or a Nigerian NGO registry (CAC) directly.")
    elif total_ng_hits == 0:
        print("RESULT: DDG returned results but none matched Nigerian-org heuristics.")
        print("The non-English queries returned results, but they appear to be")
        print("generic English pages unrelated to Nigerian organizations.")
        print("Recommended fix: Generate hybrid queries (Yoruba/Hausa term + 'Nigeria')")
        print("or query CAC (Corporate Affairs Commission) bulk data directly.")
    elif total_ng_hits < total * 2:
        print("RESULT: Weak signal - some NG-org hits but low yield.")
        print("Non-English queries return some useful results but most results are noise.")
        print("Recommended fix: Pre-filter by site:*.ng or by country-level query injection.")
    else:
        print("RESULT: Non-English queries are returning usable Nigerian org results.")
        print("The multilingual researcher pipeline should be productive for NG.")

    print("\nExp 7 complete.\n")
    return results_log


if __name__ == "__main__":
    run()
