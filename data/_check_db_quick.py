import sqlite3
db = sqlite3.connect(r'C:\Users\simon\.openclaw\workspace\commonweave\data\commonweave_directory.db')
c = db.cursor()
c.execute('SELECT COUNT(*) FROM organizations')
print('Total:', c.fetchone()[0])
c.execute("SELECT COUNT(*) FROM organizations WHERE status='active'")
print('Active:', c.fetchone()[0])
c.execute('SELECT country_code, COUNT(*) as n FROM organizations GROUP BY country_code ORDER BY n DESC LIMIT 5')
print('Top countries:', c.fetchall())
c.execute('PRAGMA table_info(organizations)')
print('Columns:', [r[1] for r in c.fetchall()])
db.close()
